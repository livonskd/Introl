namespace Introl.Assists
{
    /// <summary>
    /// Defines allowable shadow directions.
    /// </summary>
    public enum ShadowDirection
    {
        /// <summary>
        /// The shadow is directed to bottom.
        /// </summary>
        Bottom = 270,

        /// <summary>
        /// The shadow is directed to left.
        /// </summary>
        Left = 180,

        /// <summary>
        /// The shadow is directed to top.
        /// </summary>
        Top = 90,

        /// <summary>
        /// Default. The shadow is directed to right.
        /// </summary>
        Right = 0
    }
}
